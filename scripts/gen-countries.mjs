#!/usr/bin/env node
// Generates src/lib/countries.js from what's actually on disk:
// a country is AVAILABLE when public/data/<slug>/index.json exists
// (and the slug isn't in HIDDEN in src/lib/countryMeta.js).
//
// Runs automatically before `npm run build` and `npm run dev`, and after
// `scripts/import-country.mjs`. Never edit src/lib/countries.js by hand —
// edit countryMeta.js (names, flags, blurbs, order, HIDDEN) instead.
import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath, pathToFileURL } from 'node:url'

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..')
const dataDir = path.join(ROOT, 'public', 'data')
const metaPath = path.join(ROOT, 'src', 'lib', 'countryMeta.js')
const outPath = path.join(ROOT, 'src', 'lib', 'countries.js')

const { COUNTRY_META, HIDDEN } = await import(pathToFileURL(metaPath).href)

// what's on disk?
const onDisk = fs.existsSync(dataDir)
  ? fs.readdirSync(dataDir, { withFileTypes: true })
      .filter((e) => e.isDirectory() && fs.existsSync(path.join(dataDir, e.name, 'index.json')))
      .map((e) => e.name)
  : []

const titleCase = (slug) => slug.split(/[_-]+/).map((w) => w[0].toUpperCase() + w.slice(1)).join(' ')

// curated order first; then any data folders not in the meta list, alphabetically
const known = COUNTRY_META.map((m) => ({
  ...m,
  available: onDisk.includes(m.slug) && !HIDDEN.includes(m.slug),
}))
const extras = onDisk
  .filter((slug) => !COUNTRY_META.some((m) => m.slug === slug))
  .sort()
  .map((slug) => {
    // prefer the metadata set in the builder (written by import-country.mjs)
    let cj = {}
    try { cj = JSON.parse(fs.readFileSync(path.join(dataDir, slug, 'country.json'), 'utf8')) } catch { /* older imports */ }
    return {
      slug,
      name: cj.name || titleCase(slug),
      flag: cj.flag || '\u{1F30D}',
      blurb: cj.blurb || 'Mapped region by region.',
      available: !HIDDEN.includes(slug),
    }
  })

const countries = [...known, ...extras]

const lines = countries.map((c) =>
  `  { slug: '${c.slug}', name: ${JSON.stringify(c.name)}, flag: '${c.flag}', available: ${c.available},\n    blurb: ${JSON.stringify(c.blurb || '')} },`
).join('\n')

const file = `// GENERATED by scripts/gen-countries.mjs — do not edit by hand.
// Edit src/lib/countryMeta.js (names, flags, blurbs, order, HIDDEN) instead.
// A country is available when public/data/<slug>/index.json exists.
export const COUNTRIES = [
${lines}
]

export const isAvailableCountry = (slug) => COUNTRIES.some((c) => c.slug === slug && c.available)
`

fs.writeFileSync(outPath, file)
const avail = countries.filter((c) => c.available).map((c) => c.slug)
const hidden = countries.filter((c) => !c.available && onDisk.includes(c.slug)).map((c) => c.slug)
console.log(`\u2713 countries.js generated — live: ${avail.join(', ') || 'none'}${hidden.length ? ` | hidden (in HIDDEN): ${hidden.join(', ')}` : ''}`)
